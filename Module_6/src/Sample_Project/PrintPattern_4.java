public class PrintPattern_4 
{
	public static void main(String[] args) 
	{
		for(int i=1;i<=1;i++)
		{
			System.out.println(i);
			for(int j=1;j<=3;j++)
			{
				System.out.print(j + " ");
			}
			System.out.println();
			for(int k=1;k<=5;k++)
			{
				System.out.print(k + " ");
			}
		}
		System.out.println();
		for(int i=1;i>=3;i++)
		{
			System.out.print(i + " ");
			for(int j=0;j>=1;j--)
			{
				System.out.println(j+ "");
			}
		}

	}

}
